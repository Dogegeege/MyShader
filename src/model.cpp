#include "model.h"

Model::Model(const std::string verticesFile, const std::string indicesFile, const std::string propFile, const int fragSize) : fragSize(fragSize) {
    // 读取 unique_vertices.csv
    csvModLoader(verticesFile, this->vertices);
    csvModLoader(indicesFile, this->indices);
    csvModLoader(propFile, this->prop);

    std::cout << "Vertices loaded: " << vertices.size() / 8 << " vertices" << std::endl;
    std::cout << "Indices loaded: " << indices.size() << " indices" << std::endl;
    std::cout << "Prop loaded: ";
    for (const auto& p : prop) { std::cout << p << " "; }
    std::cout << std::endl;
}

